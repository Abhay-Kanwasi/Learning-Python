# convert a tuple to a string 

list_of_tuples = ('a','b','h','i')
string_of_tuples = ''.join(list_of_tuples)
print(string_of_tuples)

list_of_ch = ['a','b','h','i']
string_of_list = ''.join(list_of_ch)
print(string_of_list)

set_of_char = {'a','b','h','i'}
string_of_set = ''.join(set_of_char) #unordered so string is not in proper order
print(string_of_set)
